#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	num;

	if (n < 0)
		ft_putchar('-');
	num = (unsigned)ft_abs(n);
	if (num >= 10)
	{
		ft_putnbr(num / 10);
		ft_putnbr(num % 10);
	}
	else
		ft_putchar(num + '0');
}
